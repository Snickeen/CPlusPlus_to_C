#include<stdio.h>

/***************************************************************/
//代码8-1 利用递归调用计算二项式系数

int bino (int n,int r){
	//初始部分：n==r(选择完所有元素的情况) 或 r=0(没有可选元素的情况)
	if(r==0 || n==r) return 1;
	return bino(n-1,r-1)+bino(n-1,r); 
}
/***************************************************************/ 

int main()
{
	int n,r;
	while(~scanf("%d%d",&n,&r)){
		printf("%d\n",bino(n,r));
	}
	return 0;
}
